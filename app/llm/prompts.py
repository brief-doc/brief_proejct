"""프롬프트 템플릿 모음 — 교체 가능한 레고 블록

RAG_PROMPT / MAP_PROMPT / get_reduce_prompt() 를 교체하면
pipeline.py · summarizer.py 의 동작이 즉시 바뀝니다.

포함 항목:
    RAG_PROMPT          : RAG 답변용 ChatPromptTemplate
    MAP_PROMPT          : Map-Reduce 요약 Map 단계 프롬프트
    get_reduce_prompt() : 카테고리별 Reduce 단계 프롬프트
    CATEGORIES          : 지원 카테고리 목록
    SUMMARY_TEMPLATES   : 카테고리별 추출 항목 템플릿
"""

from langchain_core.prompts import ChatPromptTemplate

# ── RAG 시스템 규칙 ───────────────────────────────────────────────────────────
_RAG_SYSTEM = """당신은 문서 기반 질의응답 전문 AI입니다. 아래 규칙을 반드시 따르십시오.
규칙 1. [참고문서]에 있는 내용을 최대한 상세하게 활용하여 답변하십시오.
규칙 2. 관련 내용을 빠짐없이 정리하고, 항목·수치·절차·조건 등 세부 내용까지 구체적으로 설명하십시오.
규칙 3. [문서 N] 형식으로 출처를 인용하십시오. 예시: [문서 1] "지원 대상은 중소기업입니다."
규칙 4. 절대로 "문서를 참고하세요", "자세한 내용은 문서를 확인하세요" 같은 표현을 사용하지 마십시오.
         문서에 있는 내용은 직접 설명하십시오.
규칙 5. 수치·날짜·고유명사는 원문 그대로 표기하십시오.
규칙 6. [참고문서]에 질문과 관련된 내용이 전혀 없을 때만 "제공된 문서에서 해당 내용을 확인할 수 없습니다."라고 답하십시오."""

_FEW_SHOT = """
[예시 1 - 문서에 답이 있는 경우]
질문: 보조금 신청 마감일은 언제입니까?
답변: [문서 1]에 따르면 보조금 신청 마감일은 2025년 3월 31일입니다.
("신청 기간: 2025. 3. 1. ~ 3. 31.", 제3조 제1항)

[예시 2 - 문서에 답이 없는 경우]
질문: 타 지자체의 동일 사업 예산 규모는 얼마입니까?
답변: 제공된 문서에서 해당 내용을 확인할 수 없습니다.
""".strip()

# ── RAG 답변 프롬프트 (ChatPromptTemplate) ────────────────────────────────────
RAG_PROMPT = ChatPromptTemplate.from_messages(
    [
        ("system", _RAG_SYSTEM + "\n\n" + _FEW_SHOT),
        (
            "human",
            "[참고문서]\n{context}\n\n[질문]\n{question}\n\n[답변] 반드시 [문서 N] 형식으로 출처를 인용하며 합쇼체로 답변하십시오:",
        ),
    ]
)

# ── 요약 시스템 규칙 ──────────────────────────────────────────────────────────
_SUMMARY_RULES = """당신은 공공기관 행정 문서 요약 전문가입니다. 반드시 아래 규칙을 따르십시오.
규칙 1. 원문에 명시된 내용만 사용하고, 추측이나 외부 지식을 추가하지 마십시오.
규칙 2. 공공기관 행정 격식체(합쇼체: ~하십시오, ~입니다)를 사용하십시오.
규칙 3. 원문 인용 시 따옴표(" ")로 감싸고 조항·페이지를 병기하십시오.
규칙 4. 수치·날짜·고유명사는 원문 그대로 표기하십시오.
규칙 5. 해당 항목이 원문에 없으면 반드시 "해당 내용 없음"으로 표기하십시오."""

# ── 카테고리별 추출 항목 템플릿 ───────────────────────────────────────────────
SUMMARY_TEMPLATES: dict[str, str] = {
    "민사법": """\
1. 문서명 및 당사자 정보
2. 법률관계 유형 (계약, 불법행위, 손해배상, 소유권, 채권·채무 등)
3. 주요 쟁점 및 사실관계 요약
4. 적용 법조항 및 관련 판례
5. 결론 및 법적 효과 (권리·의무 변동)""",
    "행정법": """\
1. 문서명 및 관련 행정기관
2. 행정행위 유형 (허가, 인가, 처분, 취소 등)
3. 처분 내용 및 사유
4. 적용 법령 및 행정절차
5. 이의신청·행정심판·소송 등 불복 수단 및 기한""",
    "형사법": """\
1. 문서명 및 관련 사건 개요
2. 죄명 및 범죄 구성요건
3. 주요 사실관계 및 증거
4. 적용 법조항 및 처벌 규정
5. 처분·판결 결과 및 후속 절차""",
    "지식재산권": """\
1. 문서명 및 권리 유형 (특허, 저작권, 상표권, 디자인권 등)
2. 권리자 및 출원·등록·발생 정보
3. 권리 보호 범위 및 핵심 내용
4. 침해 여부·분쟁 내용 (해당 시)
5. 관련 법조항 및 조치·구제 사항""",
}

CATEGORIES: list[str] = list(SUMMARY_TEMPLATES.keys())

# ── 카테고리 자동 분류 프롬프트 ───────────────────────────────────────────────
CLASSIFY_PROMPT = ChatPromptTemplate.from_messages(
    [
        (
            "system",
            "당신은 법률 문서 분류 전문가입니다.\n"
            "아래 문서를 분석하여 다음 4가지 카테고리 중 정확히 하나로 분류하십시오.\n\n"
            "카테고리 정의:\n"
            "- 민사법: 계약, 불법행위, 손해배상, 소유권, 채권·채무 등 사인 간 법률관계\n"
            "- 행정법: 행정처분, 허가·인가·취소, 행정심판, 공공기관 행정행위\n"
            "- 형사법: 범죄, 수사, 기소, 형사재판, 형사처벌 관련\n"
            "- 지식재산권: 특허, 저작권, 상표권, 디자인권, 영업비밀\n\n"
            "반드시 위 4가지(민사법/행정법/형사법/지식재산권) 중 하나의 단어만 출력하십시오.\n"
            "다른 설명이나 문장은 절대 추가하지 마십시오.",
        ),
        (
            "human",
            "[문서 내용]\n{text}\n\n카테고리 (민사법/행정법/형사법/지식재산권 중 하나):",
        ),
    ]
)

# ── Map 단계 프롬프트 (카테고리 무관) ─────────────────────────────────────────
# ChatPromptTemplate 사용: ChatOllama / ChatHuggingFace 모두 호환
MAP_PROMPT = ChatPromptTemplate.from_messages(
    [
        ("system", _SUMMARY_RULES),
        (
            "human",
            "아래 원문 내용의 핵심 정보를 빠짐없이 추출하여 요약하십시오.\n"
            "수치·날짜·고유명사·조항 번호는 원문 그대로 표기하십시오.\n\n"
            "[원문]\n{text}\n\n"
            "위 원문을 요약하여 주십시오:",
        ),
    ]
)


def get_reduce_prompt(category: str) -> ChatPromptTemplate:
    """카테고리별 Reduce(최종 합산) 단계 프롬프트를 반환합니다.

    Args:
        category: CATEGORIES 중 하나. 없으면 '민사법' 으로 대체됩니다.
    """
    template = SUMMARY_TEMPLATES.get(category, SUMMARY_TEMPLATES["민사법"])
    return ChatPromptTemplate.from_messages(
        [
            ("system", _SUMMARY_RULES),
            (
                "human",
                f"[문서 카테고리] {category}\n\n"
                "[추출 항목]\n" + template + "\n\n"
                "아래 내용을 종합하여 위 추출 항목 형식에 맞게 최종 요약을 작성하십시오.\n\n"
                "[원문 / 부분 요약 모음]\n{text}\n\n"
                "위 내용을 바탕으로 최종 요약을 작성하여 주십시오:",
            ),
        ]
    )
